package me.croabeast.takion.bossbar;

/**
 * Represents the different elements that can be used in a bossbar.
 *
 * <p> This enum defines the types of elements that can be set as random or synchronized,
 * such as messages, colors, and styles.
 */
public enum Element {
    /**
     * Represents the titles of the bossbar.
     */
    MESSAGES,
    /**
     * Represents the colors that can be used in the bossbar.
     */
    COLORS,
    /**
     * Represents the styles that can be used in the bossbar.
     */
    STYLES
}
